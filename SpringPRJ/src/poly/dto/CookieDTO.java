package poly.dto;

public class CookieDTO {
    private String iuid;
    private String token;
	public String getIuid() {
		return iuid;
	}
	public void setIuid(String iuid) {
		this.iuid = iuid;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}

    
}
